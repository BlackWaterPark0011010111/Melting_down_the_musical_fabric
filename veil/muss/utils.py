import fitz  # PyMuPDF

def convert_pdf_to_tabs(pdf_url):
    # Открываем PDF-файл
    pdf_document = fitz.open(pdf_url)
    
    # Сначала предполагаем, что табулатуры находятся на первой странице
    page = pdf_document.load_page(0)
    
    # Извлекаем текст с первой страницы
    text = page.get_text()

    # Закрываем документ
    pdf_document.close()
    
    # В этом примере предполагается, что табулатуры в формате текста
    # Можете добавить обработку текста для получения табулатур в нужном формате
    # Например, вы можете искать конкретные паттерны текста, преобразовывать его и т.д.
    
    # Для простоты, возвращаем весь извлеченный текст
    return text
